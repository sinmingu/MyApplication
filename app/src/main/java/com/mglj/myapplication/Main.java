package com.mglj.myapplication;

public class Main {

    Double temp;  // 온도
    Integer pressure; // 압력 대기압
    Integer humidity;  // 습기
    Double temp_min; // 최저 온저
    Double temp_max; // 최대 온도


    public Double getTemp() {
        return temp;
    }

    public Integer getPressure() {
        return pressure;
    }

    public Integer getHumidity() {
        return humidity;
    }

    public Double getTemp_min() {
        return temp_min;
    }

    public Double getTemp_max() {
        return temp_max;
    }


}
